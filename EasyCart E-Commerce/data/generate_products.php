function generateProducts($category_id, $templates, $brand_ids, $adjectives, $qualities, $brands, $countries) {
    $products = [];
    
    for ($i = 0; $i < 100; $i++) {
        $template = $templates[$i % count($templates)];
        $adj = $adjectives[array_rand($adjectives)];
        $qual = $qualities[array_rand($qualities)];
        $name = "$adj {$template['name']} $qual";
        
        $base_price = rand($template['price_range'][0] * 100, $template['price_range'][1] * 100) / 100;
        $discounts = [0, 0, 0, 5, 10, 15, 20, 25, 30];
        $discount = $discounts[array_rand($discounts)];
        $price = round($base_price * (1 - $discount / 100), 2);
        
        $brand_id = $brand_ids[array_rand($brand_ids)];
        
        $product = [
            "name" => $name,
            "slug" => strtolower(str_replace(' ', '-', $name)),
            "category_id" => $category_id,
            "brand_id" => $brand_id,
            "price" => $price,
            "original_price" => round($base_price, 2),
            "discount_percent" => $discount,
            "rating" => round(rand(35, 50) / 10, 1),
            "reviews_count" => rand(10, 500),
            "stock" => rand(0, 100),
            "description" => "High-quality " . strtolower($template['name']) . " with excellent features and performance",
            "long_description" => "Experience the best in class " . strtolower($template['name']) . " with advanced technology and superior craftsmanship. Perfect for your needs.",
            "features" => [
                "High quality materials",
                "Advanced technology",
                "Durable construction",
                "Excellent performance",
                "Great value for money"
            ],
            "specifications" => [
                "Model" => sprintf("PRD-%04d", 0),
                "Brand" => $brands[$brand_id],
                "Warranty" => rand(1, 3) . " Year(s)",
                "Made In" => $countries[array_rand($countries)]
            ],
            "variants" => [
                "color" => ["Black", "White", "Blue", "Red", "Gray"],
                "size" => ($category_id == 2 || $category_id == 4) ? ["S", "M", "L", "XL"] : []
            ],
            "icon" => $template['icon'],
            "new" => rand(0, 10) > 7,
            "featured" => rand(0, 10) > 8
        ];
        
        $products[] = $product;
    }
    
    return $products;
}

